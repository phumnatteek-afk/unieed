/**
 * ค้นหาที่อยู่ตาม keyword (ตำบล / อำเภอ / รหัสไปรษณีย์)
 * @returns Array<{tambon, amphoe, province, zipcode}>
 */
export function searchAddress(keyword) {
  if (!keyword || keyword.length < 2) return [];
  const kw = keyword.trim().toLowerCase();
  const results = DB.filter(([tambon, amphoe, province, zip]) =>
    tambon.toLowerCase().includes(kw) ||
    amphoe.toLowerCase().includes(kw) ||
    zip.startsWith(kw)
  );
  return results.slice(0, 8).map(([tambon, amphoe, province, zipcode]) => ({
    tambon, amphoe, province, zipcode
  }));
}

/**
 * ค้นหาตามรหัสไปรษณีย์ (exact)
 */
export function searchByZip(zip) {
  const z = String(zip || "").trim();
  if (z.length < 4) return [];
  return DB.filter(([, , , zipcode]) => zipcode.startsWith(z))
    .slice(0, 8)
    .map(([tambon, amphoe, province, zipcode]) => ({ tambon, amphoe, province, zipcode }));
}

/**
 * filter province suggestions จากที่พิมพ์
 */
export function suggestProvinces(keyword) {
  if (!keyword) return [];
  const kw = keyword.trim().toLowerCase();
  return PROVINCES.filter(p => p.toLowerCase().includes(kw)).slice(0, 8);
}
